// generates the page title
function titleGenerator(pageurl){
	
	// here, we define where the hell the head is. So sitestuff.js knows where to dump its... well stuff. and specialtitle is the variable for telling the " - Housemoon Sprinter" part to bugger off, only a select few titles should have this condition.
	var head = document.getElementsByTagName('head')[0];
	var specialtitle = false;
	
	// now we're sure the head exists, read the title variable and create the title element
	var titlepart1 = pageurl;
	var titlegen = document.createElement('title');
	console.trace(pageurl);
	if(pageurl == museum || pageurl == google){
		specialtitle = true;
	}
	else{
		specialtitle = false;
	}
	
	// yet again another fallback, to make sure everything still looks alright.
	if(titlepart1 == undefined){
		titlegen.innerHTML = 'No Title Defined - Housemoon Sprinter';
		console.trace('pageurl has not been defined, falling back');
	}
	if(specialtitle == true){
		titlegen.innerHTML = titlepart1;
		console.trace('special pageurl detected');
	}
	// change the "RHMFSE" to the name of your site. Gives a bit of identity to your pages and helps with SEO.
	else{
		titlegen.innerHTML = titlepart1 + " - Housemoon Sprinter";
	}
	// add the title already script.
	head.appendChild(titlegen);
}

